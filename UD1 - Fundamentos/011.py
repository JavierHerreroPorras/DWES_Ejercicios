# Ejemplo de uso de type()
type(3.14)  # Devuelve <class 'float'>
type("Hola")  # Devuelve <class 'str'>
type(True)  # Devuelve <class 'bool'>
type(42)  # Devuelve <class 'int'>
type([1, 2, 3])  # Devuelve <class 'list'>
type({1, 2, 3}) # Devuelve <class 'set'>
type({"clave": "valor"})  # Devuelve <class 'dict'>

